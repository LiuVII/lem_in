#include "libft.h"

void		*ft_memalloc(size_t size)
{
	char	*tmp;
	size_t	i;

	tmp = (char*)malloc(size);
	if (!tmp)
		return (NULL);
	i = 0;
	while (i < size)
		*(tmp + i++) = 0;
	return (tmp);
}
