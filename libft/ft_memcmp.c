#include "libft.h"

int						ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned const char	*us1;
	unsigned const char	*us2;

	us1 = s1;
	us2 = s2;
	while (n--)
	{
		if (*us1 != *us2)
			return (*us1 - *us2);
		us1++;
		us2++;
	}
	return (0);
}
