#include "libft.h"

void					*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned const char	*usrc;
	unsigned char		*udst;

	udst = dst;
	usrc = src;
	while (n--)
		*udst++ = *usrc++;
	return (dst);
}
