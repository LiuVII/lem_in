#include "libft.h"

void					*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	unsigned const char	*us;

	i = 0;
	us = s;
	while (i < n)
	{
		if (*(us + i) == (unsigned char)(c))
			return ((unsigned char*)(us + i));
		i++;
	}
	return (NULL);
}
