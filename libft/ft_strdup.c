#include "libft.h"

char		*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;

	i = 0;
	while (s1[i])
		i++;
	s2 = (char*)malloc(i + 1);
	if (s2)
	{
		s2[i] = '\0';
		i = 0;
		while (s1[i++])
			s2[i - 1] = s1[i - 1];
		return (s2);
	}
	return (NULL);
}
