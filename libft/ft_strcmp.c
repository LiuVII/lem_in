#include "libft.h"

int					ft_strcmp(const char *s1, const char *s2)
{
	unsigned char	us1;
	unsigned char	us2;
	int				i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	us1 = s1[i];
	us2 = s2[i];
	return (us1 - us2);
}
